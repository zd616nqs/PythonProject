
# --------------------进制相关-----------------
# 不同进制表示
aa = 673         # 十进制
ab = 0b00101010  # 二进制 0b开头
ac = 0o34        # 八进制 0o开头
ad = 0xffffff    # 十六进制 0x开头
print(aa, ab, ac, ad)  # 673 42 28 16777215

# 进制转换
ba = 12
print(bin(ba))  # 十进制转二进制
print(oct(ba))  # 十进制转八进制
print(hex(ba))  # 十进制转十六进制


